const CONTINENTS = getWorldPaths();

export function AsciiGlobe({ size = 550, className = "" }: AsciiGlobeProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const rotYRef = useRef(0);
  const rafRef = useRef<number>(0);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const width = size;
    const height = size;
    const dpr = Math.min(window.devicePixelRatio || 1, 2);

    canvas.width = width * dpr;
    canvas.height = height * dpr;
    canvas.style.width = `${width}px`;
    canvas.style.height = `${height}px`;
    ctx.setTransform(dpr, 0, 0, dpr, 0, 0);

    const fontSize = 3.8;
    const cols = Math.floor(width / fontSize);
    const rows = Math.floor(height / (fontSize * 1.05));
    const cellW = width / cols;
    const cellH = height / rows;
    const radius = Math.min(width, height) * 0.44;
    const tilt = 0.35;

    const ASCII_CHARS = "01#*+=-:.";

    function draw() {
      ctx!.clearRect(0, 0, width, height);

      rotYRef.current += 0.0055;
      const rotY = rotYRef.current;

      const cosT = Math.cos(tilt);
      const sinT = Math.sin(tilt);
      const cosY = Math.cos(rotY);
      const sinY = Math.sin(rotY);

      ctx!.beginPath();
      ctx!.arc(width / 2, height / 2, radius, 0, Math.PI * 2);
      ctx!.strokeStyle = "rgba(0, 255, 65, 0.45)";
      ctx!.lineWidth = 1;
      ctx!.stroke();

      const zBuffer = new Float32Array(cols * rows).fill(-Infinity);
      const charBuffer = new Array(cols * rows).fill(" ");
      const randSeedBuffer = new Int32Array(cols * rows).fill(0);

      CONTINENTS.forEach((polygon) => {
        for (let p = 0; p < polygon.length - 1; p++) {
          const latA = (polygon[p][0] * Math.PI) / 180;
          const lonA = (polygon[p][1] * Math.PI) / 180;
          const latB = (polygon[p + 1][0] * Math.PI) / 180;
          const lonB = (polygon[p + 1][1] * Math.PI) / 180;

          const steps = 50;
          for (let s = 0; s <= steps; s++) {
            const t = s / steps;
            const lat = latA + (latB - latA) * t;
            const lon = lonA + (lonB - lonA) * t;

            const sinLat = Math.sin(lat);
            const cosLat = Math.cos(lat);

            const x0 = cosLat * Math.sin(lon);
            const y0 = sinLat;
            const z0 = cosLat * Math.cos(lon);

            const x1 = x0 * cosY + z0 * sinY;
            const y1 = y0;
            const z1 = -x0 * sinY + z0 * cosY;

            const x = x1;
            const y = y1 * cosT - z1 * sinT;
            const z = y1 * sinT + z1 * cosT;

            if (z > -0.05) {
              const screenX = width / 2 + x * radius;
              const screenY = height / 2 - y * radius;

              const col = Math.floor(screenX / cellW);
              const row = Math.floor(screenY / cellH);

              if (col >= 0 && col < cols && row >= 0 && row < rows) {
                const idx = row * cols + col;
                if (z > zBuffer[idx]) {
                  zBuffer[idx] = z;

                  const depthFactor = (z + 1.0) / 2.0;
                  const charIdx = Math.min(
                    ASCII_CHARS.length - 1,
                    Math.floor((1 - depthFactor) * ASCII_CHARS.length)
                  );
                  charBuffer[idx] = ASCII_CHARS[charIdx];
                  randSeedBuffer[idx] = Math.floor(Math.random() * 8);
                }
              }
            }
          }
        }
      });

      ctx!.font = `bold ${fontSize}px monospace`;
      ctx!.textAlign = "center";
      ctx!.textBaseline = "middle";

      for (let r = 0; r < rows; r++) {
        for (let c = 0; c < cols; c++) {
          const idx = r * cols + c;
          const char = charBuffer[idx];
          if (char !== " ") {
            const cx = c * cellW + cellW / 2;
            const cy = r * cellH + cellH / 2;
            const zVal = zBuffer[idx];

            const opacity = Math.max(0.25, (zVal + 0.35) / 1.35);

            if (zVal > 0.4) {
              ctx!.fillStyle = `rgba(160, 255, 175, ${opacity})`;
            } else {
              ctx!.fillStyle = `rgba(0, 220, 60, ${opacity * 0.85})`;
            }

            const finalChar =
              randSeedBuffer[idx] === 0
                ? "1"
                : randSeedBuffer[idx] === 1
                  ? "0"
                  : char;

            ctx!.fillText(finalChar, cx, cy);
          }
        }
      }

      rafRef.current = requestAnimationFrame(draw);
    }

    draw();

    return () => {
      cancelAnimationFrame(rafRef.current);
    };
  }, [size]);

  return (
    <canvas
      ref={canvasRef}
      className={`block ${className}`}
    />
  );
}

